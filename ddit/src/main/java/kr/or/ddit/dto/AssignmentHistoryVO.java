package kr.or.ddit.dto;

import java.util.Date;
import java.util.List;

public class AssignmentHistoryVO {
	private String student_id;  //수강생id
	private String assignment_history_name;  //과제제출명
	private String assignment_history_content;  //과제제출내용
	private int assignment_history_score;  //점수
	private String assignment_history_no;  //과제내역id
	private String assignment_no;  //과제번호
	private String lecture_no; //강의번호
	private Date assignment_history_reg_date; //과제 제출날짜
	
	private List<AttachToAssignVO> attachList;
	
	public String getAssignment_history_no() {
		return assignment_history_no;
	}
	public void setAssignment_history_no(String assignment_history_no) {
		this.assignment_history_no = assignment_history_no;
	}
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}

	public int getAssignment_history_score() {
		return assignment_history_score;
	}
	public void setAssignment_history_score(int assignment_history_score) {
		this.assignment_history_score = assignment_history_score;
	}
	public String getAssignment_history_content() {
		return assignment_history_content;
	}
	public void setAssignment_history_content(String assignment_history_content) {
		this.assignment_history_content = assignment_history_content;
	}
	public String getAssignment_history_name() {
		return assignment_history_name;
	}
	public void setAssignment_history_name(String assignment_history_name) {
		this.assignment_history_name = assignment_history_name;
	}

	public String getLecture_no() {
		return lecture_no;
	}
	public void setLecture_no(String lecture_no) {
		this.lecture_no = lecture_no;
	}

	public List<AttachToAssignVO> getAttachList() {
		return attachList;
	}
	public void setAttachList(List<AttachToAssignVO> attachList) {
		this.attachList = attachList;
	}
	public String getAssignment_no() {
		return assignment_no;
	}
	public void setAssignment_no(String assignment_no) {
		this.assignment_no = assignment_no;
	}
	public Date getAssignment_history_reg_date() {
		return assignment_history_reg_date;
	}
	public void setAssignment_history_reg_date(Date assignment_history_reg_date) {
		this.assignment_history_reg_date = assignment_history_reg_date;
	}
	@Override
	public String toString() {
		return "AssignmentHistoryVO [student_id=" + student_id + ", assignment_history_name=" + assignment_history_name
				+ ", assignment_history_content=" + assignment_history_content + ", assignment_history_score="
				+ assignment_history_score + ", assignment_history_no=" + assignment_history_no + ", assignment_no="
				+ assignment_no + ", lecture_no=" + lecture_no + ", assignment_history_reg_date="
				+ assignment_history_reg_date + ", attachList=" + attachList + "]";
	}

	
	
}
