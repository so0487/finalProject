package kr.or.ddit.dto;

public class CurriculumVO {
	private int curriculum_time;  //훈련시간
	private String curriculum_no;  //교육과정id
	private String curriculum_status;  //삭제여부
	private String curriculum_name;  //교육과정이름
	private String curriculum_certificate;  //관련자격증
	private String curriculum_introduce;  //교육과정소개
	
	
	
	
	
	public String getCurriculum_introduce() {
		return curriculum_introduce;
	}
	public void setCurriculum_introduce(String curriculum_introduce) {
		this.curriculum_introduce = curriculum_introduce;
	}
	public String getCurriculum_no() {
		return curriculum_no;
	}
	public void setCurriculum_no(String curriculum_no) {
		this.curriculum_no = curriculum_no;
	}
	public String getCurriculum_name() {
		return curriculum_name;
	}
	public void setCurriculum_name(String curriculum_name) {
		this.curriculum_name = curriculum_name;
	}
	public String getCurriculum_certificate() {
		return curriculum_certificate;
	}
	public void setCurriculum_certificate(String curriculum_certificate) {
		this.curriculum_certificate = curriculum_certificate;
	}
	public int getCurriculum_time() {
		return curriculum_time;
	}
	public void setCurriculum_time(int curriculum_time) {
		this.curriculum_time = curriculum_time;
	}
	public String getCurriculum_status() {
		return curriculum_status;
	}
	public void setCurriculum_status(String curriculum_status) {
		this.curriculum_status = curriculum_status;
	}
	@Override
	public String toString() {
		return "CurriculumVO [curriculum_time=" + curriculum_time + ", curriculum_no=" + curriculum_no
				+ ", curriculum_status=" + curriculum_status + ", curriculum_name=" + curriculum_name
				+ ", curriculum_certificate=" + curriculum_certificate + ", curriculum_introduce="
				+ curriculum_introduce + "]";
	}
	
	
	
	
}
