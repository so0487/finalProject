package kr.or.ddit.dto;

public class EvaToLecVO {
	private int eva_to_lec_ratio;  //평가기준비율
	private String evalution_no;  //평가기준id
	private String lecture_no;  //강의id
	private String eva_to_lec_name;  //평가기준이름
	private String eva_to_lec_no;  //평가상세id
	
	public String getEva_to_lec_no() {
		return eva_to_lec_no;
	}
	public void setEva_to_lec_no(String eva_to_lec_no) {
		this.eva_to_lec_no = eva_to_lec_no;
	}
	public String getEvalution_no() {
		return evalution_no;
	}
	public void setEvalution_no(String evalution_no) {
		this.evalution_no = evalution_no;
	}
	public String getLecture_no() {
		return lecture_no;
	}
	public void setLecture_no(String lecture_no) {
		this.lecture_no = lecture_no;
	}
	public int getEva_to_lec_ratio() {
		return eva_to_lec_ratio;
	}
	public void setEva_to_lec_ratio(int eva_to_lec_ratio) {
		this.eva_to_lec_ratio = eva_to_lec_ratio;
	}
	public String getEva_to_lec_name() {
		return eva_to_lec_name;
	}
	public void setEva_to_lec_name(String eva_to_lec_name) {
		this.eva_to_lec_name = eva_to_lec_name;
	}
	@Override
	public String toString() {
		return "EvaToLecVO [eva_to_lec_no=" + eva_to_lec_no + ", evalution_no=" + evalution_no + ", lecture_no="
				+ lecture_no + ", eva_to_lec_ratio=" + eva_to_lec_ratio + ", eva_to_lec_name=" + eva_to_lec_name + "]";
	}
	
	
}
