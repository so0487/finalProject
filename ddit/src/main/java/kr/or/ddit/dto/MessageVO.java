package kr.or.ddit.dto;

import java.sql.Date;
import java.util.List;

public class MessageVO {
	private String message_no;
	private String message_sender;
	private String message_receiver;
	private String message_title;
	private String message_content;
	private Date message_send_date;
	private int message_receiver_status;
	private int message_sender_delete;
	private int message_receiver_delete;
	
	private List<AttachToMessageVO>attachList;

	public String getMessage_no() {
		return message_no;
	}

	public void setMessage_no(String message_no) {
		this.message_no = message_no;
	}

	public String getMessage_sender() {
		return message_sender;
	}

	public void setMessage_sender(String message_sender) {
		this.message_sender = message_sender;
	}

	public String getMessage_receiver() {
		return message_receiver;
	}

	public void setMessage_receiver(String message_receiver) {
		this.message_receiver = message_receiver;
	}

	public String getMessage_title() {
		return message_title;
	}

	public void setMessage_title(String message_title) {
		this.message_title = message_title;
	}

	public String getMessage_content() {
		return message_content;
	}

	public void setMessage_content(String message_content) {
		this.message_content = message_content;
	}

	public Date getMessage_send_date() {
		return message_send_date;
	}

	public void setMessage_send_date(Date message_send_date) {
		this.message_send_date = message_send_date;
	}

	public int getMessage_receiver_status() {
		return message_receiver_status;
	}

	public void setMessage_receiver_status(int message_receiver_status) {
		this.message_receiver_status = message_receiver_status;
	}

	public int getMessage_sender_delete() {
		return message_sender_delete;
	}

	public void setMessage_sender_delete(int message_sender_delete) {
		this.message_sender_delete = message_sender_delete;
	}

	public int getMessage_receiver_delete() {
		return message_receiver_delete;
	}

	public void setMessage_receiver_delete(int message_receiver_delete) {
		this.message_receiver_delete = message_receiver_delete;
	}

	public List<AttachToMessageVO> getAttachList() {
		return attachList;
	}

	public void setAttachList(List<AttachToMessageVO> attachList) {
		this.attachList = attachList;
	}

	@Override
	public String toString() {
		return "MessageVO [message_no=" + message_no + ", message_sender=" + message_sender + ", message_receiver="
				+ message_receiver + ", message_title=" + message_title + ", message_content=" + message_content
				+ ", message_send_date=" + message_send_date + ", message_receiver_status=" + message_receiver_status
				+ ", message_sender_delete=" + message_sender_delete + ", message_receiver_delete="
				+ message_receiver_delete + ", attachList=" + attachList + "]";
	}

	
}
