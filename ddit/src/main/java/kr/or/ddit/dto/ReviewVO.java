package kr.or.ddit.dto;

public class ReviewVO {
	private String review_content;  //한줄평
	private String student_id;  //수강생id
	private Double review_star;  //별점
	private String review_no;  //수강후기번호
	private String class_no;  //회차id
	private String review_status;  //회차id
	
	
	public String getReview_status() {
		return review_status;
	}
	public void setReview_status(String review_status) {
		this.review_status = review_status;
	}
	public String getReview_content() {
		return review_content;
	}
	public void setReview_content(String review_content) {
		this.review_content = review_content;
	}
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	
	public Double getReview_star() {
		return review_star;
	}
	public void setReview_star(Double review_star) {
		this.review_star = review_star;
	}
	public String getReview_no() {
		return review_no;
	}
	public void setReview_no(String review_no) {
		this.review_no = review_no;
	}
	public String getClass_no() {
		return class_no;
	}
	public void setClass_no(String class_no) {
		this.class_no = class_no;
	}
	
	@Override
	public String toString() {
		return "ReviewVO [review_content=" + review_content + ", student_id=" + student_id + ", review_star="
				+ review_star + ", review_no=" + review_no + ", class_no=" + class_no + ", review_status="
				+ review_status + "]";
	}
	
	
	
}
