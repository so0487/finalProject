package kr.or.ddit.dto;

public class StudentVO {
	private String student_address;  //주소
	private String student_id;  //수강생id
	private String student_status;  //탈퇴여부
	private String student_role;  //권한
	private String student_mail;  //메일
	private String student_pwd;  //비밀번호
	private String student_name;  //이름
	private String student_phone;  //번호
	
	
	public String getStudent_address() {
		return student_address;
	}
	public void setStudent_address(String student_address) {
		this.student_address = student_address;
	}
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	public String getStudent_status() {
		return student_status;
	}
	public void setStudent_status(String student_status) {
		this.student_status = student_status;
	}
	public String getStudent_role() {
		return student_role;
	}
	public void setStudent_role(String student_role) {
		this.student_role = student_role;
	}
	public String getStudent_mail() {
		return student_mail;
	}
	public void setStudent_mail(String student_mail) {
		this.student_mail = student_mail;
	}
	public String getStudent_pwd() {
		return student_pwd;
	}
	public void setStudent_pwd(String student_pwd) {
		this.student_pwd = student_pwd;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getStudent_phone() {
		return student_phone;
	}
	public void setStudent_phone(String student_phone) {
		this.student_phone = student_phone;
	}
	@Override
	public String toString() {
		return "StudentVO [student_address=" + student_address + ", student_id=" + student_id + ", student_status="
				+ student_status + ", student_role=" + student_role + ", student_mail=" + student_mail
				+ ", student_pwd=" + student_pwd + ", student_name=" + student_name + ", student_phone=" + student_phone
				+ "]";
	}
	
	
	
}	
